package intro;

public class Switch {

	public static void main(String[] args) {
		String gün = "salı";
		
		switch(gün) {
			case "pazartesi":
				System.out.println("Günlerden Pazartesi");
				break;
			case "salı":
				System.out.println("Günlerden Salı");
				break;
			case "çarşamba":
				System.out.println("Günlerden Çarşamba");
				break;
			case "perşembe":
				System.out.println("Günlerden Perşembe");
				break;
		}
		

	}

}
